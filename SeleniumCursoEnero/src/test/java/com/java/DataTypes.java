package com.java;

public class DataTypes {

	public static void main(String[] args) {
		// Datos primitivos

		// Variable es un valor que puede ir cambiando

		byte var1 = 1; // Numérico entero -128 a 127
		short var2 = 1; // Numérico entero
		int var3 = 1; // Numérico entero - Automation
		long var4 = 1L; // Numerico entero para números muy grandes
		float var5 = 1.2f; // Numérico real
		double var6 = 1.223; // Numerico real (decimales) 8 bytes
		char var7 = 'a'; // Caracter
		boolean var8 = true; // boolean tiene dos valores TRUE o FALSE

		// Sintaxis son las reglas que debo respetar

		int x; // la estoy declarando
		x = 100;

		int y = 10;

		// Char symbol example
		char symbol = '\u00A9';

		// Cadena.char String no es primitivo
		String message = "Hello world";

		// Imprimir en consola
		System.out.println(message + " " + symbol); // Ctrl plus space
		System.out.println(message);

		int numero = 1;
		String nombre = "Ricardo";
		System.out.println(nombre + numero);
		// Concatenar se representa con el signo +
		
		

	}

}
